import Head from 'next/head';
import Image from 'next/image';
import { Typography, List, ListItem, ListItemAvatar, Avatar, ListItemText, Link, Box, Divider } from '@mui/material';
import PersonIcon from '@mui/icons-material/Person';
import { useState, useEffect } from 'react';
import { client, recommendedProfiles } from '../../services/getRecommendedProfiles';

export default function Home() {
    const [profiles, setProfiles] = useState([]);

    useEffect(() => {
        fetchProfiles();
    }, []);

    const fetchProfiles = async () => {
        try {
            const response = await client.query(recommendedProfiles).toPromise();
            setProfiles(response.data.recommendedProfiles);
        } catch (error) {
            console.log(error);
        }
    };

    return (
        <div>
            <Head>
                <title>Ponte Project</title>
                <meta name="description" content="Generated by create next app" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <h1>PONT DONATIONS</h1>

            <List sx={{ width: '100%', maxWidth: 360, bgcolor: 'background.paper' }}>
                {profiles.map((profile) => (
                    <Link href={`/profile/${profile.id}`} key={profile.id}>
                        <ListItem alignItems="flex-start">
                            <ListItemAvatar>
                                <Avatar>
                                    {profile.picture ? (
                                        <Image src={profile.picture.original.url} width="60px" height="60px" />
                                    ) : (
                                        <Box>
                                            <PersonIcon sx={{ color: 'white' }} />
                                        </Box>
                                    )}
                                </Avatar>
                            </ListItemAvatar>
                            <ListItemText>
                                <Typography sx={{ display: 'inline' }} component="span" variant="body2" color="text.primary">
                                    {profile.name}
                                </Typography>
                            </ListItemText>
                        </ListItem>
                        <Divider variant="inset" component="li" />
                    </Link>
                ))}
            </List>
        </div>
    );
}
